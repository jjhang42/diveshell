#include "ft_parse.h"

int	is_control_cmd(char *ptr)
{
	if (ft_strncmp("||", ptr, 2) == 0)
		return (1);
	if (ft_strncmp("&&", ptr, 2) == 0)
		return (1);
	return (0);
}
